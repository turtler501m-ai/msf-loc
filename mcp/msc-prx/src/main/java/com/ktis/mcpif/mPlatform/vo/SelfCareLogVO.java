package com.ktis.mcpif.mPlatform.vo;

public class SelfCareLogVO {
    private String mdlInd;
    private String appEventCd;
    private String ncn;
    private String url;
    private String ip;
    private String userid;

    public String getMdlInd() {
        return mdlInd;
    }

    public void setMdlInd(String mdlInd) {
        this.mdlInd = mdlInd;
    }

    public String getAppEventCd() {
        return appEventCd;
    }

    public void setAppEventCd(String appEventCd) {
        this.appEventCd = appEventCd;
    }

    public String getNcn() {
        return ncn;
    }

    public void setNcn(String ncn) {
        this.ncn = ncn;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }
}
